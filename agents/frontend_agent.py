        """
        Generate frontend code from a subtask.
        
        Args:
            subtask: Frontend subtask from decomposer
                {
                    "type": "frontend",
                    "description": "...",
                    "task_id": "...",
                    ...
                }
        
        Returns:
            Dictionary with generated code and metadata
        """
        subtask_desc = subtask.get("description", "")
        task_id = subtask.get("task_id", "")
        
        # Generate frontend code structure
        code = self._generate_streamlit_app(subtask_desc)
        code += "\n\n"
        code += self._generate_ui_components(subtask_desc)
        code += "\n\n"
        code += self._generate_state_management()
        code += "\n\n"
        code += self._generate_event_handlers(subtask_desc)
        
        return {
            "code": code,
            "language": "python",
            "agent": self.name,
            "subtask_id": task_id,
            "generated_at": datetime.now().isoformat(),
            "documentation": self._generate_documentation(subtask_desc),
            "dependencies": self._identify_dependencies(subtask_desc),
        }

