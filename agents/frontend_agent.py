class FormBuilder:
    """Builder for forms with validation"""
    def __init__(self, form_id: str):
        self.form_id = form_id
        self.fields = {}
    
    def add_text_input(self, field_name: str, label: str, required: bool = False) -> "FormBuilder":
        """Add text input field"""
        self.fields[field_name] = {
            "type": "text",
            "label": label,
            "required": required,
        }
        return self
    
    def add_number_input(self, field_name: str, label: str, min_val: int = 0, max_val: int = 100) -> "FormBuilder":
        """Add number input field"""
        self.fields[field_name] = {
            "type": "number",
            "label": label,
            "min": min_val,
            "max": max_val,
        }
        return self
    
    def add_select(self, field_name: str, label: str, options: List[str]) -> "FormBuilder":
        """Add select dropdown"""
        self.fields[field_name] = {
            "type": "select",
            "label": label,
            "options": options,
        }
        return self
    
    def render(self, on_submit: Optional[Callable] = None) -> Dict[str, Any]:
        """Render form and return values"""
        with st.form(self.form_id):
            values = {}
            
            for field_name, field_config in self.fields.items():
                field_type = field_config["type"]
                label = field_config["label"]
                
                if field_type == "text":
                    values[field_name] = st.text_input(label)
                elif field_type == "number":
                    values[field_name] = st.number_input(label, 
                                                         min_value=field_config.get("min", 0),
                                                         max_value=field_config.get("max", 100))
                elif field_type == "select":
                    values[field_name] = st.selectbox(label, field_config["options"])
            
            if st.form_submit_button("Submit"):
                if on_submit:
                    return on_submit(values)
                return values
        
        return {}


