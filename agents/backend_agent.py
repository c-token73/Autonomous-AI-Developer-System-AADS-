class BusinessLogic:
    """Core business logic dispatcher"""
    
    @staticmethod
    def validate_input(data: Dict, required_fields: List[str]) -> tuple[bool, Optional[str]]:
        """
        Validate input data.
        
        Args:
            data: Input data to validate
            required_fields: List of required field names
        
        Returns:
            Tuple of (is_valid, error_message)
        """
        if not data:
            return False, "Input data is empty"
        
        missing_fields = [field for field in required_fields if field not in data]
        if missing_fields:
            return False, f"Missing required fields: {', '.join(missing_fields)}"
        
        return True, None
    
    @staticmethod
    def process_data(data: Dict, pipeline: List[Callable]) -> Dict:
        """
        Process data through processing pipeline.
        
        Args:
            data: Input data
            pipeline: List of processing functions
        
        Returns:
            Processed data
        """
        result = data.copy()
        for processor in pipeline:
            try:
                result = processor(result)
            except Exception as e:
                result["processing_error"] = str(e)
                break
        return result
    
    @staticmethod
    def aggregate_results(results: List[Dict], aggregation_method: str = "merge") -> Dict:
        """
        Aggregate multiple results.
        
        Args:
            results: List of result dictionaries
            aggregation_method: "merge", "combine", or "chain"
        
        Returns:
            Aggregated result
        """
        if not results:
            return {"aggregated": None, "count": 0}
        
        if aggregation_method == "merge":
            merged = {}
            for result in results:
                merged.update(result)
            return {"aggregated": merged, "count": len(results)}
        
        elif aggregation_method == "combine":
            return {"aggregated": results, "count": len(results)}
        
        elif aggregation_method == "chain":
            current = results[0] if results else {}
            for result in results[1:]:
                current = {**current, **result}
            return {"aggregated": current, "count": len(results)}
        
        return {"aggregated": None, "count": 0}


