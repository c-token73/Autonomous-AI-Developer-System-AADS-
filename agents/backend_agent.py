class APIResponse:
    """Standard API response wrapper"""
    def __init__(self, status: str, data: Dict = None, error: str = None):
        self.status = status  # "success" or "error"
        self.data = data or {}
        self.error = error
        self.timestamp = datetime.now().isoformat()
    
    def to_dict(self) -> Dict:
        """Convert response to dictionary"""
        return {
            "status": self.status,
            "data": self.data,
            "error": self.error,
            "timestamp": self.timestamp,
        }


class APIEndpoint:
    """Base class for API endpoints"""
    def __init__(self, path: str, method: str = "GET"):
        self.path = path
        self.method = method
        self.handlers = {}
    
    def register_handler(self, handler_func):
        """Register handler for this endpoint"""
        self.handlers[self.method] = handler_func
        return handler_func
    
    async def handle_request(self, data: Dict = None) -> APIResponse:
        """Handle incoming request"""
        handler = self.handlers.get(self.method)
        if not handler:
            return APIResponse("error", error=f"No handler for {self.method}")
        try:
            result = await handler(data) if asyncio.iscoroutinefunction(handler) else handler(data)
            return APIResponse("success", data=result)
        except Exception as e:
            return APIResponse("error", error=str(e))


