    def _identify_dependencies(self, description: str) -> list:
        """Identify test dependencies"""
        deps = [
            "unittest",
            "pytest",
            "pytest-cov",
            "mock",
        ]
        
        # Add conditional dependencies
        if "async" in description.lower() or "concurrent" in description.lower():
            deps.append("pytest-asyncio")
        if "database" in description.lower():
            deps.append("pytest-mock")
        if "coverage" in description.lower():
            deps.append("coverage")
        
        return deps
